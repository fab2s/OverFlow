namespace fab2s\OverFlow\Concerns;

trait HasCount
{
    protected int $count = 0;

    public function getCount(): int
    {
        return $this->count;
    }

    public function setCount(int $count): static
    {
        $this->count = $count;

        return $this;
    }

    public function incrementCount(): static
    {
        $this->count++;

        return $this;
    }
}
